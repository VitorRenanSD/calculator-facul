package org.vitorrenansd.classes;

public class Operacoes {
    protected Operacoes() {
    }

    protected float adicao(float x, float y) {
        return (x + y);
    }
    protected float subtracao(float x, float y) {
        return (x - y);
    }
    protected float multiplicacao(float x, float y) {
        return (x * y);
    }
    protected float divisao(float x, float y) {
        return (x / y);
    }
}